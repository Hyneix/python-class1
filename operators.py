# Types of operators:

# 1.Arithmetic Operators:
#       +,-,*,/,%,//,**
x = 10
y = 2
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x % y)
print(x//y)  # To find the remainder
print(x**y)  # To raise the power of 1st number i.e. x

# 2.Assignment Operators:
#       =,+=,-=,*=,/=,%=,//=,**=

# 3.Comparison Operators:
#       == -> equal to
#       != -> not equal to
#       > ->greater than
#       < ->less then
#       >= ->greater than or equal to
#       <= ->less than or equal to

# 4. logical Operators:
#       and -> logic of and (both must be true)
#       or -> logic of or (either one of the option must be true)
#       not -> logic if not

# 5.Membership Operators:
#       in -> checks if a value is in sequence or not
#       (not in) -> checks if a value is not in sequences a sequence
data = ['ram', 'hari']
a = 'ram'
b = 'shyam'
print(a in data)
print(b not in data)
print()

# 6.Identity Operators :
#   is ,is not

l = 10
g = 20
f = l
print(l is g)
print(f is l)
print(g is not l)
print(f is not l)

